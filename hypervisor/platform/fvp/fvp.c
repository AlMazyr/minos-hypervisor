#include <minos/minos.h>
#include <asm/psci.h>

extern int pl011_init(void *addr);

int serial_init(void)
{
	return pl011_init((void *)0x1c090000);
}

int cpu_on(int cpu, unsigned long entry)
{
	return psci_cpu_on(cpu, entry);
}
